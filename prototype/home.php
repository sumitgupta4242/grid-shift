        function updateDateTime() {
            const now = new Date();

            // Update date with colorful styling
            const dateOptions = { year: 'numeric', month: 'long', day: 'numeric' };
            const currentDate = now.toLocaleDateString(undefined, dateOptions); // Format: January 20, 2025
            const dateElement = document.getElementById('currentDate');
            dateElement.textContent = currentDate;

            // Colorful Date Styling
            dateElement.style.color = `hsl(${Math.random() * 360}, 100%, 70%)`; // Random color

            // Update clock with colorful styling
            const hours = now.getHours().toString().padStart(2, '0');
            const minutes = now.getMinutes().toString().padStart(2, '0');
            const seconds = now.getSeconds().toString().padStart(2, '0');
            const ampm = hours >= 12 ? 'PM' : 'AM';

            const twelveHourFormat = hours % 12 || 12; // Convert to 12-hour format
            const formattedTime = `${twelveHourFormat}:${minutes}:${seconds} ${ampm}`;
            const clockElement = document.getElementById('liveClock');
            clockElement.textContent = formattedTime;
            
            // Colorful Clock Styling
            clockElement.style.color = `hsl(${Math.random() * 360}, 100%, 70%)`; // Random color
        }

        // Start updating date and clock
        updateDateTime(); // Initial call
        setInterval(updateDateTime, 1000); // Update every second

    </script>




